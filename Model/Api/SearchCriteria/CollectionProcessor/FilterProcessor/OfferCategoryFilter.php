namespace Vjackk\Offer\Model\Api\SearchCriteria\CollectionProcessor\FilterProcessor;

use Magento\Framework\Api\Filter;
use Magento\Framework\Api\SearchCriteria\CollectionProcessor\FilterProcessor\CustomFilterInterface;
use Magento\Framework\Data\Collection\AbstractDb;
use Vjackk\Offer\Model\ResourceModel\Offer\Collection;

class OfferCategoryFilter implements CustomFilterInterface
{
    /**
     * Apply custom category filter to collection
     *
     * @param Filter $filter
     * @param AbstractDb $collection
     * @return bool
     */
    public function apply(Filter $filter, AbstractDb $collection)
    {
        /** @var Collection $collection */
        $collection->addCategoryFilter($filter->getValue());

        return true;
    }
}
